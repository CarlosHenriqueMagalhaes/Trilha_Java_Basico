/*Concatenação de Strings:
Quando usamos Strings, podemos usar o + para denotar concatenação. É a única classe
que aceita algum operador fora o ponto.
 */
public class OperadoresConcatenacao {
    public static void main(String[] args) {
        String msg = "A saga";
        String quality = "é muito boa!";
        System.out.println(msg + " Star Wars " + quality);
    }
}
